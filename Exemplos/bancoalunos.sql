 SELECT AL.COD_ALUNO, AL.NOME_ALUNO, D.COD_DISCIPLINA,

         D.NOME_DISCIPLINA, ROUND(AVG(AL_AV.NOTA),1) "M dia"

    FROM ALUNO AL, DISCIPLINA D, AVALIACAO AV, ALUNO_AVALIACAO AL_AV

    WHERE AL_AV.ID_ALUNO = AL.ID_ALUNO AND

                AV.ID_AVALIACAO = AL_AV.ID_AVALIACAO  AND

                AV.ID_DISCIPLINA = D.ID_DISCIPLINA

          GROUP BY AL.COD_ALUNO, 

                   AL.NOME_ALUNO, 

                   D.COD_DISCIPLINA, 

                   D.NOME_DISCIPLINA

          ORDER BY AL.COD_ALUNO,

                   D.COD_DISCIPLINA;
                   
 SELECT AL.COD_ALUNO, AL.NOME_ALUNO, COUNT (AD.ID_DISCIPLINA) "Tot. Disciplinas"

    FROM ALUNO AL, DISCIPLINA D, ALUNO_DISCIPLINA AD

    WHERE AD.ID_ALUNO = AL.ID_ALUNO AND

          AD.ID_DISCIPLINA = D.ID_DISCIPLINA  

          GROUP BY AL.COD_ALUNO, 

                   AL.NOME_ALUNO

          ORDER BY AL.COD_ALUNO;
          
 SELECT A.COD_ALUNO,A.NOME_ALUNO, D.COD_DISCIPLINA, D.NOME_DISCIPLINA, TRUNC(AVG(NOTA),2),

       MIN(NOTA), MAX(NOTA)

    FROM ALUNO A, CIDADE C, 

         DISCIPLINA D, PROFESSOR P, CIDADE C1,

         ALUNO_AVALIACAO AL_AV , AVALIACAO AV

    WHERE A.ID_CIDADE = C.ID_CIDADE AND

          D.ID_PROFESSOR = P.ID_PROFESSOR   AND

          C1.ID_CIDADE = P.ID_CIDADE AND

          A.ID_ALUNO = AL_AV.ID_ALUNO AND

          AL_AV.ID_AVALIACAO = AV.ID_AVALIACAO AND

          AV.ID_DISCIPLINA = D.ID_DISCIPLINA

          GROUP BY A.COD_ALUNO,A.NOME_ALUNO, D.COD_DISCIPLINA, D.NOME_DISCIPLINA

          ORDER BY A.COD_ALUNO,A.NOME_ALUNO, D.COD_DISCIPLINA, D.NOME_DISCIPLINA;
